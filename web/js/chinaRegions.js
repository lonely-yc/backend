// 生成完整的省市区字符串
function getFullRegion(province, city) {
  if (!province) return '';
  if (province === '北京' || province === '天津' || province === '上海' || province === '重庆') {
    // 直辖市直接返回 "XX市"
    return province + '市';
  }
  if (province === '内蒙古') {
    return province + '自治区' + (city ? city.replace(province, '') : '');
  }
  if (province === '广西' || province === '西藏' || province === '宁夏' || province === '新疆') {
    return province + '自治区' + (city ? city.replace(province, '') : '');
  }
  if (province === '香港' || province === '澳门') {
    return province + '特别行政区';
  }
  if (province === '台湾') {
    return '台湾省' + (city || '');
  }
  return province + '省' + (city ? city : '');
}

